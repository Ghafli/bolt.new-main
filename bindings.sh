#!/bin/bash

# This script sets up bindings for the `pnpm` command, making it easier to use
# when using pnpm within the webcontainer. 
# It prevents the need to type "pnpm" every time.

alias p="pnpm"
alias pi="pnpm install"
alias pr="pnpm run"
alias pd="pnpm dev"
alias pa="pnpm add"
alias pl="pnpm lint"
alias pf="pnpm format"
